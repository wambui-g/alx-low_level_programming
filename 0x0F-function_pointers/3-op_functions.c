#include <stdio.h>
#include <stdlib.h>

/**
 * op_add- add
 * @a: no 1
 * @b: no 2
 *
 * Return: addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- substraction
 * @a: no 1
 * @b: no 2
 *
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- multiplication
 * @a: no 1
 * @b: no 2
 *
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- division
 * @a: no 1
 * @b: no 2
 *
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- modulus
 * @a: no 1
 * @b: no 2
 *
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
